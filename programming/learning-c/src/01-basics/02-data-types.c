#include <stdio.h>

int main()
{
    // Declare variables
    int age = 25;        // Integer number
    float height = 1.75; // Decimal number
    char initial = 'A';  // Single character
    char name[] = "Ana"; // Text string

    // Print variables
    printf("Name: %s\n", name);
    printf("Initial: %c\n", initial);
    printf("Age: %d\n", age);
    printf("Height: %f\n", height);

    return 0;
}
